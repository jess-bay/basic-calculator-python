def add(a, b): 
  answer = a + b
  print(str(a) + " + " + str(b) + " = " + str(answer))

def subtract(a, b): 
  answer = a - b
  print(str(a) + " - " + str(b) + " = " + str(answer))

def multiply(a, b): 
  answer = a * b
  print(str(a) + " x " + str(b) + " = " + str(answer))

def divide(a, b): 
  answer = a / b
  print(str(a) + " / " + str(b) + " = " + str(answer))

